(
    `APPROVALFILE_ID`          BIGINT       NOT NULL AUTO_INCREMENT COMMENT '결재파일코드',
    `APPROVALFILE_APPROVAL_ID` BIGINT       NOT NULL COMMENT '결재코드',
    `APPROVALFILE_NAME`        VARCHAR(255) NOT NULL COMMENT '결재파일이름',
    `APPROVALFILE_ORIGINNAME`  VARCHAR(255) NOT NULL COMMENT '결재파일원본이름',
    `APPROVALFILE_PATH`        VARCHAR(255) NOT NULL COMMENT '결재파일경로',
    PRIMARY KEY (`APPROVALFILE_ID`)
